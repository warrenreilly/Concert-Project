function changeTheme() {


    var body = document.getElementById("body");
    var headTxt = document.getElementById("head");

body.className = "body2";
headTxt.className = "h12";


}

function changeBack() {


    var body = document.getElementById("body");
    var headTxt = document.getElementById("head");

body.className = "body1";
headTxt.className = "h1";


}


